  A["会用 AI 写代码"] --> B["基本配置，不稀缺"]
  C["能判断 AI 写的代码能不能用"] --> D["很值钱，在升值"]
  E["能定义 AI 该写什么、为什么写"] --> F["最稀缺，持续升值"]
